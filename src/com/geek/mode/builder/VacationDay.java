package com.geek.mode.builder;

public class VacationDay {
	private int date;
	private String addr;
	
	public VacationDay(int date) {
		this.date = date;
	}

	public int getDate() {
		return date;
	}

	public void setDate(int date) {
		this.date = date;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}
}
